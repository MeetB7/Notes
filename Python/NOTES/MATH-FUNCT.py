import math  # to perform math we have to import math 'module'
pi = 3.14
npi = -3.14
x = 1
y = 2
z = 3

print(round(pi))  # this will round the number to the closest integer
print(math.ceil(pi))  # this will round the number up
print(math.floor(pi))  # this will round down
print(abs(npi))  # this will give the absolute value (how far number is away from zero)
print(pow(pi,2))  # adds exponent
print(math.sqrt(pi))
print(max(x,y,z))
print(min(x,y,z))